/**
 * The dynamic-script emitter for the GAN harness (cutover P3, item 15).
 *
 * The SW emitter (`gan-fixture-generator.ts`) builds a 6-node spec whose refine
 * step is a `for`/`while` loop over jq-evaluated state. This emitter re-expresses
 * the SAME flow as JavaScript, reusing the EXACT persona instructions and prompts
 * from `fragments/personas.ts` so the two producers cannot drift on the parts that
 * actually drive the agents.
 *
 * What changes (and why the script is simpler):
 *  - the jq `while` gate + `.loop.*` feedback channel become a plain `while` loop
 *    over local variables;
 *  - the deterministic build gate and verdict parse become plain JS (no jq);
 *  - `dev/preview` keeps its durable activation contract: `action()` routes
 *    preview-native activations to the action_runner child, which reuses the SW
 *    interpreter's ready-set poll (cutover blocker B1).
 *
 * Generated — never hand-edit the emitted script (same rule as the SW fixture).
 */
import type { GanFixtureConfig } from "./gan-config";
import { buildGateNode } from "./fragments/gate";
import {
	CRITIC_INSTRUCTIONS,
	DESIGN_REVIEW_INSTRUCTIONS,
	PLANNER_GENERATOR_INSTRUCTIONS,
	critiquePrompt,
	designReviewPrompt,
	generatePrompt,
	planPrompt,
} from "./fragments/personas";

/** `meta` for the emitted script (name/description/phases + the trigger schema). */
export function ganScriptMeta(cfg: GanFixtureConfig): Record<string, unknown> {
	return {
		name: cfg.name,
		description: cfg.summary,
		phases: [
			{ title: "Dev mode" },
			{ title: "Plan" },
			{ title: "Refine" },
			{ title: "Promote" },
		],
		input: {
			type: "object",
			properties: {
				intent: { type: "string", title: "What to build/change" },
				service: { type: "string", default: cfg.defaults.service },
				generatorAgent: { type: "string", default: cfg.defaults.generatorAgent },
				criticAgent: { type: "string", default: cfg.defaults.criticAgent },
				maxIterations: { type: "number", default: cfg.defaults.maxIterations },
			},
		},
	};
}

export function generateGanScript(cfg: GanFixtureConfig): string {
	const d = cfg.defaults;
	const J = (value: unknown) => JSON.stringify(value);
	// The SW gate node is the source of truth for the gate shell — reuse it and
	// swap its jq placeholders for JS interpolation tokens.
	const gateNode = buildGateNode(cfg) as { gate: { with: Record<string, unknown> } };
	const gateWith = gateNode.gate.with;
	const rawGate = String(gateWith.command ?? "");
	// buildCommand() wrapped the script in a jq string-concat expression; recover
	// the shell body + re-mark the three substitution points.
	const gateBody = rawGate
		.replace(/^\$\{\s*"/, "")
		.replace(/"\s*\}$/, "")
		.replace(/"\s*\+\s*\(?\s*\.idx \| tostring\s*\)?\s*\+\s*"/g, "__IDX__")
		.replace(/"\s*\+\s*\(\(\.enter_dev_mode\.url \/\/ ""\) \+ "\/__export"\)\s*\+\s*"/g, "__EXPORT_URL__")
		.replace(/"\s*\+\s*\(\.enter_dev_mode\.syncCapability \/\/ ""\)\s*\+\s*"/g, "__SYNC_TOKEN__");
	const gateCommandLiteral = JSON.stringify(
		gateBody.replace(/\\n/g, "\n").replace(/\\"/g, '"'),
	);

	return [
		`export const meta = ${JSON.stringify(ganScriptMeta(cfg), null, 2)}`,
		"",
		"// GENERATED by scripts/fixtures/generator-critic/gen/gan-script-generator.ts —",
		"// never hand-edit. Personas/prompts come from fragments/personas.ts, shared",
		"// verbatim with the SW emitter so the two producers cannot drift.",
		`const GATE_COMMAND = ${gateCommandLiteral}`,
		"const t = args ?? {}",
		`const service = t.service ?? ${J(d.service)}`,
		`const generatorAgent = t.generatorAgent ?? ${J(d.generatorAgent)}`,
		`const criticAgent = t.criticAgent ?? ${J(d.criticAgent)}`,
		`const maxIterations = Number(t.maxIterations ?? ${d.maxIterations}) || ${d.maxIterations}`,
		"",
		"phase('Dev mode')",
		"// preview-native adopt → the runner child's durable ready-set poll (B1).",
		"const preview = await action('dev/preview', {",
		"  service,",
		"  mode: 'preview-native',",
		"  adopt: true,",
		`  timeoutSeconds: ${d.timeouts.previewTimeoutSeconds},`,
		`  waitReadySeconds: ${d.timeouts.previewWaitReadySeconds},`,
		"}, { label: 'enter_dev_mode' })",
		"",
		"const PLAN_SANDBOX = { workspaceRef: workspace, cwd: '/sandbox/scratch' }",
		"",
		"phase('Plan')",
		`const plan = await agent(${J(planPrompt())}, {`,
		"  label: 'plan',",
		"  agent: generatorAgent,",
		"  isolation: 'shared',",
		"  sandbox: { ...PLAN_SANDBOX, maxTurns: 12, timeoutMinutes: 20 },",
		"})",
		"",
		`const designReview = await agent(${J(designReviewPrompt())}, {`,
		"  label: 'design_review',",
		"  agent: criticAgent,",
		"  isolation: 'shared',",
		"  sandbox: { ...PLAN_SANDBOX, maxTurns: 8, timeoutMinutes: 15 },",
		"})",
		"",
		"phase('Refine')",
		"// The SW `for` + jq `while` gate becomes a plain loop: iterate until the",
		"// critic accepts or the iteration cap is hit (the same bound the spec used).",
		"const VERDICT_SCHEMA = {",
		"  type: 'object',",
		"  required: ['accepted'],",
		"  properties: {",
		"    accepted: { type: 'boolean' },",
		"    score: { type: 'number' },",
		"    failing: { type: 'array', items: { type: 'string' } },",
		"    summary: { type: 'string' },",
		"  },",
		"}",
		"",
		"let accepted = false",
		"let iterations = 0",
		"let lastVerdict = null",
		"let lastGenerator = null",
		"",
		"while (!accepted && iterations < maxIterations) {",
		"  const feedback = lastVerdict",
		"    ? `\\n\\nPREVIOUS CRITIC VERDICT (address every failing item):\\n${JSON.stringify(lastVerdict)}`",
		"    : ''",
		"",
		`  lastGenerator = await agent(${J(generatePrompt())} + feedback, {`,
		"    label: `generate #${iterations + 1}`,",
		"    phase: 'Refine',",
		"    agent: generatorAgent,",
		"    isolation: 'shared',",
		"    sandbox: { ...PLAN_SANDBOX, maxTurns: 40, timeoutMinutes: 45 },",
		"  })",
		"",
		"  // Deterministic build gate — the SAME shell the SW emitter builds",
		"  // (fragments/gate.ts); its jq placeholders become JS interpolation.",
		"  const gateCommand = GATE_COMMAND",
		"    .split('__IDX__').join(String(iterations))",
		"    .split('__EXPORT_URL__').join((preview?.url ?? '') + '/__export')",
		"    .split('__SYNC_TOKEN__').join(preview?.syncCapability ?? '')",
		"  const gate = await action('workspace/command', {",
		"    cliWorkspace: true,",
		"    helperPod: true,",
		`    helperTimeoutMinutes: ${gateWith.helperTimeoutMinutes ?? 240},`,
		"    workspaceRef: workspace,",
		"    command: gateCommand,",
		`    cwd: ${J(gateWith.cwd ?? "/sandbox/work")},`,
		`    timeoutMs: ${Number(gateWith.timeoutMs ?? 1500000)},`,
		"  }, { label: `gate #${iterations + 1}`, phase: 'Refine', allowFailure: true })",
		"",
		"  const gateOk = (gate?.exitCode ?? 1) === 0",
		"  if (!gateOk) {",
		"    lastVerdict = {",
		"      accepted: false,",
		"      failing: ['build gate failed: ' + String(gate?.stderr ?? gate?.stdout ?? '').slice(0, 800)],",
		"      summary: 'build gate failed',",
		"    }",
		"    iterations += 1",
		"    continue",
		"  }",
		"",
		`  lastVerdict = await agent(${J(critiquePrompt())}, {`,
		"    label: `critique #${iterations + 1}`,",
		"    phase: 'Refine',",
		"    agent: criticAgent,",
		"    isolation: 'shared',",
		"    schema: VERDICT_SCHEMA,",
		"    sandbox: { ...PLAN_SANDBOX, maxTurns: 25, timeoutMinutes: 30 },",
		"  })",
		"",
		"  accepted = lastVerdict?.accepted === true",
		"  iterations += 1",
		"}",
		"",
		"phase('Promote')",
		"// Promote → PR only on acceptance (the SW spec gated this with jq).",
		"let promote = null",
		"if (accepted) {",
		"  promote = await action('dev/preview-promote', {",
		"    service,",
		"    title: t.intent ?? 'GAN: accepted change',",
		"  }, { label: 'promote', allowFailure: true })",
		"}",
		"",
		"return {",
		"  accepted,",
		"  iterations,",
		"  service,",
		"  preview,",
		"  verdict: lastVerdict,",
		"  generatorSummary: typeof lastGenerator === 'string' ? lastGenerator.slice(0, 4000) : lastGenerator,",
		"  designReview: typeof designReview === 'string' ? designReview.slice(0, 4000) : designReview,",
		"  plan: typeof plan === 'string' ? plan.slice(0, 4000) : plan,",
		"  promote,",
		"}",
		"",
	].join("\n");
}

/** Persona instructions are the agents' system prompts — exported so the seed can
 * upsert the same agent definitions the SW fixture relied on. */
export const GAN_SCRIPT_PERSONAS = {
	planner: PLANNER_GENERATOR_INSTRUCTIONS,
	designReview: DESIGN_REVIEW_INSTRUCTIONS,
	critic: CRITIC_INSTRUCTIONS,
};

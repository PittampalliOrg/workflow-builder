import { readFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { describe, expect, it } from "vitest";

describe("MCP connection route", () => {
	it("keeps MCP connection mutations behind workflow-data application services", () => {
		const source = readFileSync(
			join(dirname(fileURLToPath(import.meta.url)), "+server.ts"),
			"utf8",
		);

		expect(source).toContain("workflowData.updateProjectMcpConnection");
		expect(source).toContain("workflowData.deleteProjectMcpConnection");
		expect(source).not.toContain("$lib/server/db");
		expect(source).not.toContain("$lib/server/db/schema");
		expect(source).not.toContain("drizzle-orm");
		expect(source).not.toContain("mcpConnections");
		expect(source).not.toContain("validateMcpCredentialBinding");
	});
});
